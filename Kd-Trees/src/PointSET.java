import edu.princeton.cs.algs4.Point2D;
import edu.princeton.cs.algs4.RectHV;
import edu.princeton.cs.algs4.SET;
import edu.princeton.cs.algs4.StdDraw;

import java.util.ArrayList;

/**
 * Created by saurabh on 3/19/17.
 */
public class PointSET {

    private SET<Point2D> point2DSet;

    public PointSET() {
        point2DSet = new SET<>();
    }

    public boolean isEmpty() {
        return point2DSet.isEmpty();
    }

    public int size() {
        return point2DSet.size();
    }

    public void insert(Point2D p) {
        if (p == null)
            throw new NullPointerException();

        point2DSet.add(p);
    }

    public boolean contains(Point2D p) {
        if (p == null)
            throw new NullPointerException();

        return point2DSet.contains(p);
    }

    public void draw() {

        StdDraw.setPenColor(StdDraw.BLACK);
        StdDraw.setPenRadius(0.01);

        for (Point2D point2D : point2DSet)
            point2D.draw();

    }

    public Iterable<Point2D> range(RectHV rect) {
        if (rect == null)
            throw new NullPointerException();

        ArrayList<Point2D> list = new ArrayList<>();

        for (Point2D point2D : point2DSet) {
            if (rect.contains(point2D))
                list.add(point2D);
        }

        return list;
    }

    public Point2D nearest(Point2D p) {
        if (p == null)
            throw new NullPointerException();

        double smallestDist = 2;
        Point2D nearest = null;

        for (Point2D point2D : point2DSet) {

            double distance = p.distanceSquaredTo(point2D);

            if (distance < smallestDist) {
                smallestDist = distance;
                nearest = point2D;
            }
        }

        return isEmpty() ? null : nearest;
    }

    public static void main(String[] args) {

    }
}
